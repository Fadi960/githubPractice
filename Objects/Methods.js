// keys,values,entries

// object keys
const obj = { a: 1, b: 2, c: 3 };
const keys = Object.keys(obj);
console.log(keys); 

// object values
const obj2 = {
    a:1,
    b:2,
    c:3
};
const values = Object.values(obj2);
console.log(values);

// object entries

const obj3 = {
    a:1,
    b:2,
    c:3
};
const entries = Object.entries(obj3);
console.log(entries);